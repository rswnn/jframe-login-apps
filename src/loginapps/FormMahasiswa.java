        String comboBox = comboJurusan.getSelectedItem().toString();
        String alamat = textAlamat.getText();
        
        runSQL("UPDATE mhs SET name =  '"+nama+"', nim = '"+nim+"', comBox = '"+comboBox+"', alamat = '"+alamat+"' WHERE nim = '"+nim+"'");
