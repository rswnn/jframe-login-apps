    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        int baris = jTable2.rowAtPoint(evt.getPoint());
        
        String nama =jTable2.getValueAt(baris, 0).toString();
        textNama.setText(nama);
        String nim = jTable2.getValueAt(baris,1).toString();
        textNim.setText(nim);
 
        String jr = jTable2.getValueAt(baris, 2).toString();
        comboJurusan.setSelectedItem(jr);
        String alamat=jTable2.getValueAt(baris, 3).toString();
        textAlamat.setText(alamat);
        
    }//GEN-LAST:event_jTable2MouseClicked

